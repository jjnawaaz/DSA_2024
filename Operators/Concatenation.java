package Operators;

public class Concatenation {
    public static void main(String[] args) {
        int x = 5, y = 8;
        System.out.println("Concatenation (x + y) = "+x+y);   // Concatenation of 5 and 8
        System.out.println("Addition (x + y) = "+(x+y));      // Addition of 5 + 8 = 13
    }
}
